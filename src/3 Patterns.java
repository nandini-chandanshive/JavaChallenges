class Patterns {
    public static void main(String[] args) {

        //RightHalfPyramid
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****\n");

        //ReverseRightHalfPyramid
        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*\n");

        //LeftHalfPyramid
        System.out.println("    *");
        System.out.println("   **");
        System.out.println("  ***");
        System.out.println(" ****");
        System.out.println("*****\n");

        //ReverseLeftHalfPyramid
        System.out.println("*****");
        System.out.println(" ****");
        System.out.println("  ***");
        System.out.println("   **");
        System.out.println("    *\n");

    }
}
